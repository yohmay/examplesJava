public class DifferenceNumbers {

  public static void main(String[] args) {
    int number1 = 10;
    int number2 = 11;

    if (number1 == number2) { // If they are equal, THEN RETURN TRUE
      System.out.println("The numbers are equal");
    } else {
      System.out.println("Too bad, the numbers are NOT equal");
    }
  }
}
